    Write-Error "chain-spec.sepolia.json missing under testnet/"
}
Write-Success "Signed Sepolia chain spec ready for nginx bind mounts"


function Invoke-IpfsVolumePerms {
    param([string[]]$ComposeArgs)
    Write-Host "Running ipfs-perms (chown 1000:100, clear stale repo.lock)..." -ForegroundColor DarkGray
    $permsArgs = $ComposeArgs + @("run", "--rm", "--no-deps", "ipfs-perms")
    docker compose @permsArgs
    if ($LASTEXITCODE -ne 0) {
        Write-Error @"
ipfs-perms failed. IPFS Kubo needs uid 1000 on the volume.
Try:  .\testnet\start-3node-test.ps1 -FreshVolumes
Or:   docker volume rm testnet_ipfs-data
"@
    }
}
Write-Step "Starting infrastructure (IPFS, spec-server)"
Invoke-IpfsVolumePerms -ComposeArgs @(Get-ComposeArgs)
docker compose @(Get-ComposeArgs @("up", "-d", "ipfs", "spec-server"))
if ($LASTEXITCODE -ne 0) {
    Write-Error "docker compose failed to start infrastructure"
}

if (-not (Wait-ComposeHealthy -ContainerName "creg-3node-ipfs" -MaxSec 180)) {
    docker compose -f $composeFile logs --tail 40 ipfs ipfs-perms | Write-Host
    Write-Host ""
    Write-Host "If you see 'repo.lock: permission denied', reset IPFS data:" -ForegroundColor Yellow
    Write-Host "  .\testnet\start-3node-test.ps1 -FreshVolumes" -ForegroundColor Yellow
    Write-Error "IPFS container did not become healthy"
}
Write-Success "IPFS ready"

$specHostPort = if ($env:CREG_3NODE_SPEC_HOST_PORT) { $env:CREG_3NODE_SPEC_HOST_PORT } else { "18888" }
$specUrl = "http://localhost:$specHostPort/chain-spec.json"
$specReady = $false
$specDeadline = (Get-Date).AddSeconds(60)
while ((Get-Date) -lt $specDeadline) {
    if ((docker inspect --format '{{.State.Health.Status}}' creg-3node-spec-server 2>$null) -eq "healthy") {
        $specReady = $true
        break
    }
    try {
        $probe = Invoke-WebRequest -Uri $specUrl -UseBasicParsing -TimeoutSec 5
        if ($probe.StatusCode -eq 200 -and $probe.Content -match '"chain_id"') {
            $specReady = $true
            break
        }
    } catch { }
    Start-Sleep -Seconds 2
}
if (-not $specReady) {
    docker compose -f $composeFile logs --tail 20 spec-server | Write-Host
    Write-Error "spec-server did not become healthy (probe: $specUrl)"
}
Write-Success "spec-server ready at http://localhost:$specHostPort/chain-spec.json"

