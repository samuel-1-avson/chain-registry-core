Write-Host "  IPFS API:        http://localhost:$ipfsHostPort"
$specHostPort = if ($env:CREG_3NODE_SPEC_HOST_PORT) { $env:CREG_3NODE_SPEC_HOST_PORT } else { "18888" }
Write-Host "  Spec Server:     http://localhost:$specHostPort/chain-spec.json"
