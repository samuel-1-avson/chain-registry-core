# Chain Registry 3-Node Sepolia Test Orchestrator
# Runs a 3-node fleet (2 validators + observer) against live Ethereum Sepolia.
#
# Prerequisites:
#   - Docker Desktop / Docker Engine running
#   - testnet/sepolia-3node.env (copy from sepolia-3node.env.example)
#   - testnet/chain-spec.sepolia.json.sig present
#
# Usage:
#   .\testnet\start-3node-test.ps1
#   .\testnet\start-3node-test.ps1 -RpcUrl "https://ethereum-sepolia-rpc.publicnode.com"
#   .\testnet\start-3node-test.ps1 -SkipRpcVerify
#   .\testnet\start-3node-test.ps1 -FreshVolumes   # reset Docker volumes (fixes IPFS repo.lock)

param(
    [string]$RpcUrl = "",
    [switch]$SkipRpcVerify,
    [switch]$FreshVolumes,
    [switch]$Sandbox
)

$ErrorActionPreference = "Stop"

$Script:SepoliaFallbackRpcUrls = @(
    "https://ethereum-sepolia-rpc.publicnode.com",
    "https://rpc.ankr.com/eth_sepolia",
    "https://1rpc.io/sepolia"
)

$scriptDir = Split-Path -Parent $MyInvocation.MyCommand.Path
$repoRoot = Split-Path -Parent $scriptDir
Set-Location $repoRoot

$composeFile = Join-Path $scriptDir "docker-compose.3node.yml"
$composeSandboxFile = Join-Path $scriptDir "docker-compose.3node-sandbox.yml"
function Get-ComposeArgs {
    param([string[]]$Extra)
    $args = @("-f", $composeFile)
    if ($Sandbox) { $args += @("-f", $composeSandboxFile) }
    if (Test-Path $fleetEnv) { $args += @("--env-file", $fleetEnv) }
    if ($Extra) { $args += $Extra }
    return $args
}
$fleetEnv = Join-Path $scriptDir "sepolia-3node.env"
$fleetEnvExample = Join-Path $scriptDir "sepolia-3node.env.example"
$legacySepoliaEnv = Join-Path $scriptDir ".env.sepolia"
$specPath = Join-Path $scriptDir "chain-spec.sepolia.json"
$sigPath = Join-Path $scriptDir "chain-spec.sepolia.json.sig"

function Write-Step($msg) {
    Write-Host ""
    Write-Host "=== $msg ===" -ForegroundColor Cyan
}

function Write-Success($msg) {
    Write-Host "✓ $msg" -ForegroundColor Green
}

function Write-Warn($msg) {
    Write-Host "⚠ $msg" -ForegroundColor Yellow
}

function Import-DotEnvFile {
    param([string]$Path)
    if (-not (Test-Path $Path)) { return }
    Get-Content $Path | ForEach-Object {
        $line = $_.Trim()
        if ($line -eq "" -or $line.StartsWith("#")) { return }
        if ($line -match '^([^=]+)=(.*)$') {
            $key = $matches[1].Trim()
            $val = $matches[2].Trim()
            if ($val.StartsWith('"') -and $val.EndsWith('"')) {
                $val = $val.Substring(1, $val.Length - 2)
            }
            Set-Item -Path "Env:$key" -Value $val
        }
    }
}

function Write-FleetEnvFile {
    param(
        [string]$Rpc,
        [string]$Key1,
        [string]$Key2
    )
    @(
        "# Auto-generated by start-3node-test.ps1 - edit or replace with sepolia-3node.env.example copy"
        "SEPOLIA_RPC_URL=$Rpc"
        "CREG_ETH_RPC=$Rpc"
        "CREG_VALIDATOR_KEY_1=$Key1"
        "CREG_VALIDATOR_KEY_2=$Key2"
    ) | Set-Content -Path $fleetEnv -Encoding utf8
}

function Merge-SepoliaEnv {
    if (Test-Path $fleetEnv) {
        Import-DotEnvFile -Path $fleetEnv
        Write-Success "Loaded $fleetEnv"
        return
    }
    if (Test-Path $legacySepoliaEnv) {
        Write-Warn "sepolia-3node.env missing; merging from .env.sepolia"
        Import-DotEnvFile -Path $legacySepoliaEnv
        if (-not $env:CREG_ETH_RPC -and $env:SEPOLIA_RPC_URL) {
            $env:CREG_ETH_RPC = $env:SEPOLIA_RPC_URL
        }
        if (-not $env:CREG_VALIDATOR_KEY_1 -and $env:CREG_VALIDATOR_KEY) {
            $env:CREG_VALIDATOR_KEY_1 = $env:CREG_VALIDATOR_KEY
        }
        if ($env:CREG_ETH_RPC -and $env:CREG_VALIDATOR_KEY_1 -and $env:CREG_VALIDATOR_KEY_2) {
            Write-FleetEnvFile -Rpc $env:CREG_ETH_RPC -Key1 $env:CREG_VALIDATOR_KEY_1 -Key2 $env:CREG_VALIDATOR_KEY_2
            Write-Success "Wrote $fleetEnv from .env.sepolia for docker compose env_file"
        }
        return
    }
    Write-Error @"
Missing fleet env file. Create it from the example:
  Copy-Item '$fleetEnvExample' '$fleetEnv'
Then set SEPOLIA_RPC_URL, CREG_ETH_RPC, CREG_VALIDATOR_KEY_1, and CREG_VALIDATOR_KEY_2.
"@
}

function Test-RpcUrlPlaceholder {
    param([string]$Url)
    return ($Url -match 'YOUR_|REPLACE|example\.com|/v3/$')
}

function Invoke-EthJsonRpc {
    param(
        [string]$RpcUrl,
        [string]$Method,
        [object[]]$Params = @(),
        [int]$TimeoutSec = 45
    )
    $body = @{
        jsonrpc = "2.0"
        method  = $Method
        params  = $Params
        id      = 1
    } | ConvertTo-Json -Compress
    return Invoke-RestMethod -Uri $RpcUrl -Method Post -Body $body -ContentType "application/json" -TimeoutSec $TimeoutSec
}

function Test-SepoliaRpc {
    param(
        [string]$RpcUrl,
        [int]$TimeoutSec = 45,
        [int]$Retries = 3
    )
    $lastError = $null
    for ($attempt = 1; $attempt -le $Retries; $attempt++) {
        try {
            $resp = Invoke-EthJsonRpc -RpcUrl $RpcUrl -Method "eth_chainId" -TimeoutSec $TimeoutSec
            if (-not $resp.result) {
                throw "eth_chainId returned no result"
            }
            $chainId = [Convert]::ToInt64($resp.result, 16)
            if ($chainId -ne 11155111) {
                throw "reported chain_id $chainId (expected 11155111)"
            }
            return
        } catch {
            $lastError = $_.Exception.Message
            if ($attempt -lt $Retries) {
                Write-Warn "RPC attempt $attempt/$Retries failed for $RpcUrl - retrying..."
                Start-Sleep -Seconds 2
            }
        }
    }
    throw "RPC $RpcUrl failed after $Retries attempts: $lastError"
}

function Resolve-SepoliaRpc {
    param([string]$PreferredUrl)
    if (Test-RpcUrlPlaceholder -Url $PreferredUrl) {
        throw @"
CREG_ETH_RPC looks like a template URL: $PreferredUrl
Set a real Sepolia endpoint in testnet/sepolia-3node.env, or run:
  .\testnet\init-sepolia-3node-env.ps1 -Force
  .\testnet\start-3node-test.ps1 -RpcUrl `"https://ethereum-sepolia-rpc.publicnode.com`"
"@
    }
    try {
        Test-SepoliaRpc -RpcUrl $PreferredUrl
        return $PreferredUrl
    } catch {
        Write-Warn "Primary RPC failed: $($_.Exception.Message)"
    }
    foreach ($fallback in $Script:SepoliaFallbackRpcUrls) {
        if ($fallback -eq $PreferredUrl) { continue }
        Write-Warn "Trying fallback RPC: $fallback"
        try {
            Test-SepoliaRpc -RpcUrl $fallback
            Write-Warn "Using fallback RPC for this run: $fallback"
            return $fallback
        } catch {
            Write-Warn "Fallback failed: $($_.Exception.Message)"
        }
    }
    throw "No working Sepolia RPC. Edit CREG_ETH_RPC in sepolia-3node.env or pass -RpcUrl."
}

function Wait-ComposeHealthy {
    param(
        [string]$ContainerName,
        [int]$MaxSec = 120
    )
    $deadline = (Get-Date).AddSeconds($MaxSec)
    while ((Get-Date) -lt $deadline) {
        $status = docker inspect --format '{{if .State.Health}}{{.State.Health.Status}}{{else}}{{.State.Status}}{{end}}' $ContainerName 2>$null
        if ($status -eq "healthy" -or $status -eq "running") {
            return $true
        }
        Start-Sleep -Seconds 2
    }
    return $false
}

