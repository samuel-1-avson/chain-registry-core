}
foreach ($pair in @(
    @("CREG_3NODE_NODE1_API_PORT", "28180"),
    @("CREG_3NODE_NODE1_P2P_PORT", "29100"),
    @("CREG_3NODE_NODE2_API_PORT", "28181"),
    @("CREG_3NODE_NODE2_P2P_PORT", "29101"),
    @("CREG_3NODE_NODE3_API_PORT", "28182"),
    @("CREG_3NODE_NODE3_P2P_PORT", "29102")
)) {
    if (-not (Get-Item -Path "Env:$($pair[0])" -ErrorAction SilentlyContinue).Value) {
        Set-Item -Path "Env:$($pair[0])" -Value $pair[1]
    }
}

if (-not $env:CREG_ETH_RPC) {
    if ($env:SEPOLIA_RPC_URL) {
        $env:CREG_ETH_RPC = $env:SEPOLIA_RPC_URL
    } else {
        Write-Error "CREG_ETH_RPC (or SEPOLIA_RPC_URL) must be set in sepolia-3node.env"
    }
}

foreach ($key in @("CREG_VALIDATOR_KEY_1", "CREG_VALIDATOR_KEY_2")) {
    $val = (Get-Item -Path "Env:$key" -ErrorAction SilentlyContinue).Value
    if (-not $val -or $val -match 'REPLACE|YOUR_INFURA') {
        Write-Host ""
        Write-Host "Fleet env still has placeholders in $fleetEnv" -ForegroundColor Yellow
        Write-Host "Run:  .\testnet\init-sepolia-3node-env.ps1" -ForegroundColor Yellow
        Write-Host "Or edit SEPOLIA_RPC_URL, CREG_ETH_RPC, CREG_VALIDATOR_KEY_1, CREG_VALIDATOR_KEY_2" -ForegroundColor Yellow
        Write-Error "$key must be a 32-byte Ed25519 seed hex (not a placeholder)"
    }
}
if ($RpcUrl) {
    $env:CREG_ETH_RPC = $RpcUrl
    $env:SEPOLIA_RPC_URL = $RpcUrl
}

if (Test-RpcUrlPlaceholder -Url $env:CREG_ETH_RPC) {
    Write-Host "Run:  .\testnet\init-sepolia-3node-env.ps1 -Force" -ForegroundColor Yellow
    Write-Host "Or:   .\testnet\start-3node-test.ps1 -RpcUrl `"https://ethereum-sepolia-rpc.publicnode.com`"" -ForegroundColor Yellow
    Write-Error "CREG_ETH_RPC is still a template URL: $($env:CREG_ETH_RPC)"
}

if (-not $SkipRpcVerify) {
    Write-Step "Verifying Sepolia RPC (eth_chainId == 11155111)"
    try {
        $resolvedRpc = Resolve-SepoliaRpc -PreferredUrl $env:CREG_ETH_RPC
        $env:CREG_ETH_RPC = $resolvedRpc
        $env:SEPOLIA_RPC_URL = $resolvedRpc
        if (Test-Path $fleetEnv) {
            $lines = Get-Content $fleetEnv
            $lines = $lines | ForEach-Object {
                if ($_ -match '^\s*SEPOLIA_RPC_URL\s*=') { "SEPOLIA_RPC_URL=$resolvedRpc" }
                elseif ($_ -match '^\s*CREG_ETH_RPC\s*=') { "CREG_ETH_RPC=$resolvedRpc" }
                else { $_ }
            }
            $lines | Set-Content -Path $fleetEnv -Encoding utf8
        }
        Write-Success "Sepolia RPC OK: $resolvedRpc"
    } catch {
        Write-Error $_.Exception.Message
    }
} else {
    Write-Warn "Skipping Sepolia RPC verification (-SkipRpcVerify)"
}

if (-not (Test-Path $specPath)) {
    Write-Error "Missing $specPath"
}
if (-not (Test-Path $sigPath)) {
    Write-Error "Missing $sigPath - run .\testnet\finalize-sepolia-spec.ps1 or copy a signed spec"
}
Write-Success "Signed Sepolia chain spec present"

# Step 1: Clean up previous test runs (keep volumes unless -FreshVolumes passed later)
Write-Step "Cleaning up previous test runs"
$downArgs = @(Get-ComposeArgs @("down", "--remove-orphans"))
if ($FreshVolumes) {
    $downArgs += "-v"
    Write-Warn "FreshVolumes: removing named volumes (ipfs-data, creg-data-*)"
}
docker compose @downArgs 2>$null
Write-Success "Previous containers removed"

# Step 2: Ensure spec-server mount files are current, start infrastructure
Write-Step "Verifying chain-spec.sepolia.json for spec-server mounts"
